#include "./so_long.h"

void	open_game_exit(char **map)
{
	int	x;
	int	y;

	y = 0;
	while (map[y])
	{
		x = 0;
		while (map[y][x])
		{
			if (map[y][x] == 'E')
				map[y][x] = 'e';
			x++;
		}
		y++;
	}
}
